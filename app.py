def ask_claude(question, passages, api_key, model):
    import anthropic
    client = anthropic.Anthropic(api_key=api_key)

    context = "\n\n".join(
        f"[{i+1} | {p['source']} p.{p['page']}] {p['text']}" for i, p in enumerate(passages)
    )
    system_prompt = (
        "Answer using ONLY the passages given. Be precise and brief: short "
        "sentences or a tight bullet list, no filler, no restating the question. "
        "Cite passages inline like [1], [2]. If the passages don't answer the "
        "question, say so in one sentence."
    )
    msg = client.messages.create(
        model=model,
        max_tokens=400,
        system=system_prompt,
        messages=[{"role": "user", "content": f"Passages:\n\n{context}\n\nQuestion: {question}"}],
    )
    return "".join(b.text for b in msg.content if b.type == "text")


def show_passages_directly(passages):
    if not passages:
        return "No passage met the confidence threshold for this question."
    lines = ["**Top matching passages:**"]
    for i, p in enumerate(passages, start=1):
        snippet = p["text"].strip()
        if len(snippet) > 260:
            snippet = snippet[:260].rsplit(" ", 1)[0] + " …"
        lines.append(f"**[{i}] {p['source']} p.{p['page']}** ({p['confidence']*100:.0f}% match) — {snippet}")
    return "\n\n".join(lines)


def confidence_color(c):
    if c >= 0.7:
        return "#0ea5e9"
    if c >= 0.4:
        return "#38bdf8"
    return "#94a3b8"


