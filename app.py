# PDF PARSING
# =========================================================
def parse_pdf(file):
    """Return list of (page_number, cleaned_text)."""
    reader = PdfReader(file)
    pages = []
    for i, page in enumerate(reader.pages, start=1):
        text = page.extract_text() or ""
        text = re.sub(r"[ \t]+", " ", text)
        text = re.sub(r"\n{2,}", "\n", text).strip()
        if text:
            pages.append((i, text))
    return pages


SENTENCE_SPLIT = re.compile(r"(?<=[.!?])\s+(?=[A-Z0-9])")


def sentence_aware_chunks(text, source, page, size, overlap):
    """Split on sentence boundaries, then pack sentences into ~size-char
    chunks with overlap, so chunks don't cut mid-sentence."""
    sentences = [s.strip() for s in SENTENCE_SPLIT.split(text) if s.strip()]
    if not sentences:
        return []

    chunks, current, current_len = [], [], 0
    for sent in sentences:
        if current_len + len(sent) > size and current:
            chunks.append(" ".join(current))
            # carry the tail of the previous chunk forward for overlap
            tail, tail_len = [], 0
            for s in reversed(current):
                if tail_len >= overlap:
                    break
                tail.insert(0, s)
                tail_len += len(s)
            current, current_len = tail[:], tail_len
        current.append(sent)
        current_len += len(sent)
    if current:
        chunks.append(" ".join(current))

    return [{"text": c, "source": source, "page": page} for c in chunks if len(c) > 25]


def tokenize(text):
    return re.findall(r"[a-z0-9]+", text.lower())


class SimpleBM25:
    """A small, dependency-free BM25 (Okapi) implementation — avoids relying
    on an external package that may not get installed on some deploy
    targets. Only needs numpy, which Streamlit already depends on."""

    def __init__(self, tokenized_docs, k1=1.5, b=0.75):
        self.k1 = k1
        self.b = b
        self.docs = tokenized_docs
        self.doc_lens = np.array([len(d) for d in tokenized_docs], dtype=float)
        self.avgdl = self.doc_lens.mean() if len(tokenized_docs) else 0.0

        self.doc_freqs = []  # per-doc term counts
        df = {}  # document frequency per term
        for doc in tokenized_docs:
            counts = {}
            for term in doc:
                counts[term] = counts.get(term, 0) + 1
            self.doc_freqs.append(counts)
            for term in counts:
                df[term] = df.get(term, 0) + 1

        n_docs = len(tokenized_docs)
        self.idf = {
            term: np.log(1 + (n_docs - freq + 0.5) / (freq + 0.5))
            for term, freq in df.items()
        }

    def get_scores(self, query_tokens):
        scores = np.zeros(len(self.docs))
        for term in query_tokens:
            idf = self.idf.get(term)
            if idf is None:
                continue
            for i, counts in enumerate(self.doc_freqs):
                f = counts.get(term, 0)
                if f == 0:
                    continue
                denom = f + self.k1 * (1 - self.b + self.b * self.doc_lens[i] / (self.avgdl or 1))
                scores[i] += idf * (f * (self.k1 + 1)) / denom
        return scores


def build_bm25_index(chunks):
    tokenized = [tokenize(c["text"]) for c in chunks]
    return SimpleBM25(tokenized), tokenized


def normalize_scores(scores):
    """Squash raw BM25 scores into 0-1 via their max, so the confidence
    slider means roughly the same thing regardless of corpus size."""
    scores = np.array(scores, dtype=float)
    top = scores.max() if scores.size else 0.0
    if top <= 0:
        return np.zeros_like(scores)
    return np.clip(scores / top, 0, 1)


def retrieve(question, bm25, chunks, k, min_conf):
    raw_scores = bm25.get_scores(tokenize(question))
    conf = normalize_scores(raw_scores)

    order = np.argsort(conf)[::-1]
    pool = order[: max(k * 3, 15)]
    candidates = sorted(i for i in pool if conf[i] >= min_conf)

    # merge an immediately-following chunk from the same source/page group
    # into the passage when it's also a candidate, for continuous context
    used, merged = set(), []
    for idx in candidates:
        if idx in used:
            continue
        group = [idx]
        used.add(idx)
        nxt = idx + 1
        if nxt < len(chunks) and nxt in candidates and chunks[nxt]["source"] == chunks[idx]["source"]:
            group.append(nxt)
            used.add(nxt)
        merged.append({
            "text": " ".join(chunks[i]["text"] for i in group),
            "source": chunks[idx]["source"],
            "page": chunks[idx]["page"],
            "confidence": float(max(conf[i] for i in group)),
        })

    merged.sort(key=lambda p: p["confidence"], reverse=True)
    return merged[:k]


